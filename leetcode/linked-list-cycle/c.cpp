/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    bool hasCycle(ListNode *head) {
        ListNode* fast = head;
        ListNode* slow = head;
        if (NULL == head) return false;
        while(fast && fast->next){
            fast = fast->next->next;
            slow = slow->next;
            if(NULL != fast && slow == fast)
                return true;
        }
        return false;
    }
};